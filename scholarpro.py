# --- RESEARCH ENGINES (OpenAlex + ArXiv Fallback) ---
async def fetch_real_data(query):
    encoded_query = urllib.parse.quote(query)

    # 1. PRIMARY: OpenAlex (No key required, high reliability)
    openalex_url = f"https://api.openalex.org/works?search={encoded_query}&limit=10"
    try:
        async with aiohttp.ClientSession() as session:
            async with session.get(openalex_url, timeout=8) as response:
                if response.status == 200:
                    data = await response.json()
                    results = []
                    for item in data.get('results', []):
                        results.append({
                            "Title": item.get('display_name', 'Untitled'),
                            "Authors": ", ".join(a.get('author', {}).get('display_name', '') for a in item.get('authorships', [])[:3]),
                            "Year": str(item.get('publication_year', 'N/A')),
                            "Abstract": "Detailed context available for AI analysis." if item.get('abstract_inverted_index') else "No abstract provided.",
                            "URL": item.get('doi') or item.get('id'),
                            "Citations": f"{item.get('cited_by_count', 0)} (OpenAlex)"
                        })
                    if results: return pd.DataFrame(results)
    except:
        pass

    # 2. FALLBACK: ArXiv
    arxiv_url = f"http://export.arxiv.org/api/query?search_query=all:{encoded_query}&max_results=10"
    try:
        async with aiohttp.ClientSession() as session:
            async with session.get(arxiv_url, timeout=10) as resp:
                feed = feedparser.parse(await resp.text())
                results = []
                for entry in feed.entries:
                    results.append({
                        "Title": entry.title.replace('\n', ' ').strip(),
                        "Authors": ", ".join(a.name for a in entry.authors) if hasattr(entry, 'authors') else "Unknown",
                        "Year": entry.published[:4] if hasattr(entry, 'published') else "N/A",
                        "Abstract": entry.summary.replace('\n', ' ').strip(),
                        "URL": entry.link,
                        "Citations": "N/A (ArXiv)"
                    })
                return pd.DataFrame(results)
    except Exception:
        return pd.DataFrame([{"Title": "No results found.", "Authors": "-", "Year": "-", "Citations": "-"}])

# --- RAG CHAT LOGIC (Updated Model) ---
def run_ai_chat(question, paper_data):
    if not GROQ_CLIENT: return "⚠️ AI Error: 'scholarApi' API key missing in Secrets."
    if not paper_data or 'Abstract' not in paper_data: return "⚠️ Please select a paper from results first!"

    context = paper_data.get('Abstract', "No context available.")
    prompt = f"Context: {context}\n\nQuestion: {question}\n\nAnalyze the context and answer the question precisely:"

    try:
        # Using the recommended Llama-3.1 replacement model
        completion = GROQ_CLIENT.chat.completions.create(
            model="llama-3.1-8b-instant",
            messages=[{"role": "system", "content": "You are a professional research assistant."},
                      {"role": "user", "content": prompt}],
            temperature=0.1
        )
        return completion.choices[0].message.content
    except Exception as e:
        return f"AI Error: {str(e)}"

# --- UI REFRESH LOGIC ---
def save_and_refresh(paper_data):
    if not paper_data or 'Title' not in paper_data: return "⚠️ Selection required!", None
    with sqlite3.connect(DB_PATH) as conn:
        conn.execute("INSERT INTO saved_papers (title, authors, year, abstract, url) VALUES (?,?,?,?,?)",
                  (paper_data['Title'], paper_data['Authors'], paper_data['Year'], paper_data['Abstract'], paper_data['URL']))
    new_lib = pd.read_sql_query("SELECT id, title, year FROM saved_papers", sqlite3.connect(DB_PATH))
    return "✅ Saved to Personal Library!", new_lib


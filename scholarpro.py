    refresh_btn.click(lambda: pd.read_sql_query("SELECT id, title, year FROM saved_papers", sqlite3.connect(DB_PATH)), outputs=lib_df)
    ask_btn.click(run_ai_chat, inputs=[chat_input, selected_paper_state], outputs=chat_output)

